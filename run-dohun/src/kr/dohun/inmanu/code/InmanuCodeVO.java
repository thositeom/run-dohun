package kr.dohun.inmanu.code;

public class InmanuCodeVO {
	private String topCode;
	private String topCodeName;
	private String topCodeDesc;
	private String subCode;
	private String subCodeName;
	private String subCodeDesc;
	private String subCodeOrder;

	public String getTopCode() {
		return topCode;
	}
	public void setTopCode(String topCode) {
		this.topCode = topCode;
	}
	public String getTopCodeName() {
		return topCodeName;
	}
	public void setTopCodeName(String topCodeName) {
		this.topCodeName = topCodeName;
	}
	public String getTopCodeDesc() {
		return topCodeDesc;
	}
	public void setTopCodeDesc(String topCodeDesc) {
		this.topCodeDesc = topCodeDesc;
	}
	public String getSubCode() {
		return subCode;
	}
	public void setSubCode(String subCode) {
		this.subCode = subCode;
	}
	public String getSubCodeName() {
		return subCodeName;
	}
	public void setSubCodeName(String subCodeName) {
		this.subCodeName = subCodeName;
	}
	public String getSubCodeDesc() {
		return subCodeDesc;
	}
	public void setSubCodeDesc(String subCodeDesc) {
		this.subCodeDesc = subCodeDesc;
	}
	public String getSubCodeOrder() {
		return subCodeOrder;
	}
	public void setSubCodeOrder(String subCodeOrder) {
		this.subCodeOrder = subCodeOrder;
	}
}